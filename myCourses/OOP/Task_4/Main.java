/*
Задача:
	— Создать класс "УчительСервис" и реализовать аналогично проделанному на семинаре;
	— Создать класс "УчительВью" и реализовать аналогично проделанному на семинаре;
	— Создать класс "УчительКонтроллер" и реализовать возможность создания, редактирования конкретного учителя и отображения списка учителей, имеющихся в системе.
*/

import java.util.ArrayList;
import java.util.List;

class Учитель {
    private String имя;
    private String предмет;

    public Учитель(String имя, String предмет) {
        this.имя = имя;
        this.предмет = предмет;
    }

    public String getИмя() {
        return имя;
    }

    public String getПредмет() {
        return предмет;
    }

    public void setИмя(String имя) {
        this.имя = имя;
    }

    public void setПредмет(String предмет) {
        this.предмет = предмет;
    }

    @Override
    public String toString() {
        return "Учитель: " + имя + ", предмет: " + предмет;
    }
}

class УчительСервис {
    private List<Учитель> учителя = new ArrayList<>();

    public List<Учитель> получитьУчителей() {
        return учителя;
    }

    public void добавитьУчителя(Учитель учитель) {
        учителя.add(учитель);
    }

    public void обновитьУчителя(String староеИмя, String новоеИмя, String новыйПредмет) {
        for (Учитель учитель : учителя) {
            if (учитель.getИмя().equals(староеИмя)) {
                учитель.setИмя(новоеИмя);
                учитель.setПредмет(новыйПредмет);
                break;
            }
        }
    }
}

class УчительВью {
    public void показатьСписокУчителей(List<Учитель> учителя) {
        for (Учитель учитель : учителя) {
            System.out.println(учитель);
        }
    }
}

class УчительКонтроллер {
    private УчительСервис учительСервис;
    private УчительВью учительВью;

    public УчительКонтроллер(УчительСервис учительСервис, УчительВью учительВью) {
        this.учительСервис = учительСервис;
        this.учительВью = учительВью;
    }

    public void создатьУчителя(String имя, String предмет) {
        Учитель учитель = new Учитель(имя, предмет);
        учительСервис.добавитьУчителя(учитель);
    }

    public void редактироватьУчителя(String староеИмя, String новоеИмя, String новыйПредмет) {
        учительСервис.обновитьУчителя(староеИмя, новоеИмя, новыйПредмет);
    }

    public void показатьСписокУчителей() {
        List<Учитель> учителя = учительСервис.получитьУчителей();
        учительВью.показатьСписокУчителей(учителя);
    }
}

public class Main {
    public static void main(String[] args) {
        УчительСервис учительСервис = new УчительСервис();
        УчительВью учительВью = new УчительВью();
        УчительКонтроллер учительКонтроллер = new УчительКонтроллер(учительСервис, учительВью);

        // Создание учителей
        учительКонтроллер.создатьУчителя("Иванов", "Математика");
        учительКонтроллер.создатьУчителя("Петров", "Физика");
        учительКонтроллер.создатьУчителя("Сидоров", "История");

        // Вывод списка учителей
        учительКонтроллер.показатьСписокУчителей();

        // Редактирование учителя
        учительКонтроллер.редактироватьУчителя("Иванов", "Смирнов", "Биология");

        // Вывод обновленного списка учителей
        учительКонтроллер.показатьСписокУчителей();
    }
}
